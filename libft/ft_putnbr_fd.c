#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char num;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (0 <= n && n <= 9)
	{
		num = n + (int)'0';
		write(fd, &num, 1);
		return ;
	}
	ft_putnbr_fd(n / 10, fd);
	ft_putnbr_fd(n % 10, fd);
}
